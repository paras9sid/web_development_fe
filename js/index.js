// switch statements

let rating = 13;

switch(rating){
    case 5:
        console.log("Must watch!");
        break;
    case 4:
        console.log("good opt!");
        break;
    case 3:
        console.log("Maybe");
        break;
    case 2:
        console.log("Skip!");
        break;
    case 1:
        console.log("Ignore!");
        break;
    default:
        console.log("Invalid!");
}